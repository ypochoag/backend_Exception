public class ExceptionVariados extends Exception {

    /**
     * Creates a new instance of <code>ExceptionVariados</code> without detail
     * message.
     */
    public ExceptionVariados() {
    }

    /**
     * Constructs an instance of <code>ExceptionVariados</code> with the
     * specified detail message.
     *
     * @param msg the detail message.
     */
    public ExceptionVariados(String msg) {
        super(msg);
    }
}
